package com.java8.exercise.model;

public class Product {
    int i;
    String orange;

    public Product(int i, String orange) {
        this.i = i;
        this.orange = orange;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public String getOrange() {
        return orange;
    }

    public void setOrange(String orange) {
        this.orange = orange;
    }
}
